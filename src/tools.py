def _text(content: str) -> dict:
    return {"content": [{"type": "text", "text": content}]}


def _extract_local_filters(args: dict) -> dict:
    filters = {}
    if args.get("country"):
        filters["country"] = args["country"]
    if args.get("seniority"):
        filters["seniority"] = args["seniority"]
    if args.get("industry"):
        filters["industry"] = args["industry"]
    if args.get("has_email"):
        filters["has_email"] = True
    return filters


@tool(
    "local_search",
    "FIRST search tool for every new search request. "
    "Search the local database for free. "
    "Always call this before parse_search_query or pdl_search. "
    "Only include optional filters you already know.",
    LOCAL_SEARCH_SCHEMA,
)
async def local_search_tool(args: dict) -> dict:
    query = args["query"].strip()
    page = args.get("page", 0)
    size = args.get("size", 10)
    filters = _extract_local_filters(args)

    print(f"[local_search] query={query!r}, filters={filters}, page={page}, size={size}")

    try:
        result = await run_local_search(query, filters=filters, page=page, size=size)
        result["source"] = "local"
        return _text(json.dumps(result, ensure_ascii=False, indent=2))
    except Exception as e:
        print(f"[local_search] error: {e}")
        return _text(json.dumps({
            "total": 0,
            "people": [],
            "has_more": False,
            "page": page,
            "source": "none",
            "message": f"Local search error: {e}",
        }, ensure_ascii=False, indent=2))


@tool(
    "parse_search_query",
    "Generate a PDL SQL query from the user's natural-language description. "
    "Use this only after local_search returned total=0 or a local-search error.",
    PARSE_SEARCH_QUERY_SCHEMA,
)
async def parse_search_query_tool(args: dict) -> dict:
    try:
        params = parse_query(args["query"])
        return _text(json.dumps(params, ensure_ascii=False, indent=2))
    except (json.JSONDecodeError, IndexError) as e:
        return _text(f"Error parsing query: {e}")


@tool(
    "pdl_search",
    "Search the global People Data Labs database with a SQL query. "
    "Consumes PDL search quota. "
    "Use this only after local_search returned 0 results and parse_search_query produced sql_query.",
    PDL_SEARCH_SCHEMA,
)
async def pdl_search_tool(args: dict) -> dict:
    params = {
        "sql_query": args["sql_query"],
        "size": args.get("size", 10),
    }
    if args.get("scroll_token"):
        params["scroll_token"] = args["scroll_token"]

    try:
        results = await pdl_search_people(params)
        people = results.get("people", [])

        if people:
            try:
                await save_pdl_to_people(people, source="pdl_search")
            except Exception:
                pass

        output = {
            "total": results.get("total_entries", 0),
            "people": people,
            "has_more": bool(results.get("scroll_token")) or len(people) >= params["size"],
            "page": 0,
            "source": "pdl",
            "scroll_token": results.get("scroll_token"),
        }
        if results.get("error"):
            output["message"] = results["error"]
        return _text(json.dumps(output, ensure_ascii=False, indent=2))
    except Exception as e:
        return _text(json.dumps({
            "total": 0,
            "people": [],
            "has_more": False,
            "page": 0,
            "source": "none",
            "message": f"PDL search error: {e}",
        }, ensure_ascii=False, indent=2))


@tool(
    "score_and_filter_results",
    "Use LLM to score, filter, and summarize search results based on user intent and scenario. "
    "Use this after local_search or pdl_search when the result set is noisy or large.",
    SCORE_AND_FILTER_SCHEMA,
)
async def score_and_filter_tool(args: dict) -> dict:
    try:
        people = json.loads(args["people_json"])
    except json.JSONDecodeError as e:
        return _text(f"Invalid people JSON: {e}")

    threshold = args.get("threshold", 4)
    scenario = args.get("scenario", "recruiting")
    try:
        result = score_and_summarize(args["user_query"], people, threshold, scenario)
        return _text(json.dumps(result, ensure_ascii=False, indent=2))
    except (json.JSONDecodeError, IndexError) as e:
        return _text(f"Scoring error: {e}")


