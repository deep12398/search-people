@tool(
    "pdl_enrich",
    "Get full profile details for one person via PDL enrichment. "
    "Consumes 1 credit per successful lookup.",
    PDL_ENRICH_SCHEMA,
)
async def pdl_enrich_tool(args: dict) -> dict:
    params = {k: v for k, v in args.items() if v}
    if not params:
        return _text("Need linkedin_url, email, or name+company.")

    if not params.get("linkedin_url") and not params.get("email"):
        if not (params.get("name") and params.get("company")):
            return _text("Need linkedin_url, email, or name+company.")

    try:
        result = await enrich_person(params)
        if "error" in result:
            return _text(f"Enrich error: {result['error']}")

        try:
            await save_pdl_to_people([result], source="pdl_enrich")
        except Exception:
            pass

        return _text(json.dumps(result, ensure_ascii=False, indent=2))
    except Exception as e:
        return _text(f"PDL enrich error: {e}")


