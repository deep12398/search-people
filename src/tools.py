    """Create the MCP server with all people search tools."""
    return create_sdk_mcp_server(
        "people-search-tools",
        tools=[
            local_search_tool,
            parse_search_query_tool,
            pdl_search_tool,
            score_and_filter_tool,
            auto_relax_params_tool,
            suggest_narrowing_tool,
            pdl_enrich_tool,
        ],
    )
